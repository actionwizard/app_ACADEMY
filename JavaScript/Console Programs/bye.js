//A Java Script function that takes in a string name and returns a string saying "bye" to that name.

function bye(name) {
  return "Bye " + name + ".";
}

//Some example Test Cases
console.log(bye("Daniel")); // => "Bye Daniel."
console.log(bye("Mark")); // => "Bye Mark."
console.log(bye("Beyonce")); // => "Bye Beyonce."
